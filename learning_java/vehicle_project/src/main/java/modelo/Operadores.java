public class Operadores extends Usuarios{
    private String login;
    private String senha;

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
    
    public Operadores(String nome, String endereco, String login,String senha){
        super(nome, endereco);
        this.login = login;
        this.senha = senha;
    }
}
